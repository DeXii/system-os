export function buildCatalogSeedBooks(): Omit<LibraryBook, 'status'>[] {
  const out: Omit<LibraryBook, 'status'>[] = [];
  for (const group of BOOK_LEVEL_GROUPS) {
    for (const item of group.items) {
      out.push({
        id: `catalog-${group.level}-${slug(item.title)}`,
        level: group.level,
        title: item.title,
        author: item.author,
        tags: item.tags,
        description: item.description,
        source: 'catalog',
      });
    }
  }
  return out;
}
